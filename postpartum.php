            <h2 class="featurette-heading" style="color:#727273">Postpartum care: What to expect after a vaginal birth
            </h2>
            <br>
            <p style="color: black;" class="lead1">Your newborn might be your priority — but postpartum care counts, too. From soreness to discharge, what to expect as you recover from a vaginal delivery.
            </p>
            <div>
                <p class="lead1">Pregnancy changes your body in more ways than you might expect. And it doesn't stop when the baby is born. Here's what to expect physically and emotionally after a vaginal delivery.</p>
            </div>
            <br>
            <div id="vaginalsoreness">
                <h3 class="featurette-heading" style="color:#7d671f;">Vaginal soreness
                </h3>

                <p class="lead1">If you had a vaginal tear during delivery or your doctor made an incision, the wound might hurt for a few weeks. Extensive tears might take longer to heal. To ease discomfort while you're recovering:
                </p>
                <ul id="postpartumli">
                    <li>Sit on a pillow or padded ring.</li>
                    <li>Cool the area with an ice pack, or place a chilled witch hazel pad between a sanitary napkin and the area between your vaginal opening and anus (perineum).</li>
                    <li>Use a squeeze bottle to pour warm water over the perineum as you're passing urine.</li>
                    <li>Sit in a warm bath just deep enough to cover your buttocks and hips for five minutes. Use cold water if you find it more soothing.</li>
                    <li>Take an over-the-counter pain reliever. Ask your health care provider about a numbing spray or cream, if needed.</li>
                    <li>Talk to your health care provider about using a stool softener or laxative to prevent constipation.</li>
                </ul>
                <p class="lead1">Tell your health care provider if you're experiencing severe, persistent or increasing pain. It could be a sign of infection.</p>
            </div>

            <br>
            <div id="vaginaldischarge">
                <h3 class="featurette-heading" style="color:#7d671f;">Vaginal Discharge
                </h3>

                <p class="lead1">After delivery, you'll begin to shed the superficial mucous membrane that lined your uterus during pregnancy. You'll have vaginal discharge made up of this membrane and blood for weeks. The discharge will be red and heavy for the first few days. Then it will taper, become increasingly watery and change from pinkish brown to yellowish white.</p>

                <p class="lead1">Contact your health care provider if you have heavy vaginal bleeding — soaking a pad in less than an hour — especially if it's accompanied by pelvic pain, a fever or tenderness.</p>
            </div>

            <br>
            <div id="contractions">
                <h3 class="featurette-heading" style="color:#7d671f;">Contractions
                </h3>

                <p class="lead1">You might feel occasional contractions, sometimes called afterpains, during the first few days after delivery. These contractions — which often resemble menstrual cramps — help prevent excessive bleeding by compressing the blood vessels in the uterus. Afterpains are common during breastfeeding due to the release of the hormone oxytocin. Your health care provider might recommend an over-the-counter pain reliever.</p>
            </div>

            <br>
            <div id="incontinence">
                <h3 class="featurette-heading" style="color:#7d671f;">Incontinence
                </h3>

                <p class="lead1">Pregnancy, labor and a vaginal delivery can stretch or injure your pelvic floor muscles, which support the uterus, bladder and rectum. This might cause you to leak a few drops of urine while sneezing, laughing or coughing. These problems usually improve within weeks but might persist long term.
                </p>

                <p class="lead1">In the meantime, wear sanitary pads and do pelvic floor muscle exercises (Kegels) to help tone your pelvic floor muscles and control your bladder. To do Kegels, imagine you are sitting on a marble and tighten your pelvic muscles as if you're lifting the marble. Try it for three seconds at a time, then relax for a count of three. Work up to doing the exercise 10 to 15 times in a row, at least three times a day.</p>
            </div>

            <br>
            <div id="hemorrhoids">
                <h3 class="featurette-heading" style="color:#7d671f;">Hemorrhoids and bowel movements
                </h3>

                <p class="lead1">If you notice pain during bowel movements and feel swelling near your anus, you might have swollen veins in the anus or lower rectum (hemorrhoids). To ease discomfort while the hemorrhoids heal:
                </p>
                <ul id="postpartumli">
                    <li>Apply an over-the-counter hemorrhoid cream or suppository containing hydrocortisone.</li>
                    <li>Use pads containing witch hazel or a numbing agent.</li>
                    <li>Soak your anal area in plain warm water for 10 to 15 minutes two to three times a day.</li>
                </ul>
                <p class="lead1">If you find yourself avoiding bowel movements out of fear of hurting your perineum or aggravating the pain of hemorrhoids or your episiotomy wound, take steps to keep your stools soft and regular. Eat foods high in fiber — including fruits, vegetables and whole grains — and drink plenty of water. Ask your health care provider about a stool softener, if needed.</p>
            </div>

            <br>
            <div id="tender">
                <h3 class="featurette-heading" style="color:#7d671f;">Tender breasts
                </h3>

                <p class="lead1">A few days after birth, you might experience full, firm, tender breasts (engorgement). Frequent breastfeeding on both breasts is recommended to avoid or minimize engorgement.
                </p>
                <p class="lead1">If your breasts — including the dark circles of skin around the nipples — are engorged, your baby might have difficulty latching. To help your baby latch, you might hand express or use a breast pump to express a small amount of breast milk before feeding your baby. To ease breast discomfort, apply warm washcloths or take a warm shower before breastfeeding or expressing. It might make milk removal easier. Between feedings, place cold washcloths on your breasts. Over-the-counter pain relievers might help, too.</p>
                <p class="lead1">If you're not breastfeeding, wear a supportive bra, such as a sports bra. Don't pump your breasts or express the milk, which will cause your breasts to produce more milk.</p>
            </div>

            <br>
            <div id="hairloss">
                <h3 class="featurette-heading" style="color:#7d671f;">Hair loss and skin changes
                </h3>

                <p class="lead1">During pregnancy, elevated hormone levels mean your hair grows faster than it sheds. The result is often an extra-lush head of hair — but now it's payback time. After delivery, you'll experience hair loss for up to five months.
                </p>

                <p class="lead1">Stretch marks won't disappear after delivery, but eventually they'll fade from red to silver. Expect any skin that darkened during pregnancy — such as dark patches on your face — to slowly fade as well.</p>
            </div>

            <br>
            <div id="moodchanges">
                <h3 class="featurette-heading" style="color:#7d671f;">Mood changes
                </h3>

                <p class="lead1">Childbirth triggers a jumble of powerful emotions. Many new moms experience a period of feeling down or anxious, sometimes called the baby blues. Symptoms include mood swings, crying spells, anxiety and difficulty sleeping. The baby blues typically subside within two weeks. In the meantime, take good care of yourself. Share your feelings, and ask your partner, loved ones or friends for help.
                </p>

                <p class="lead1">If you experience severe mood swings, loss of appetite, overwhelming fatigue and lack of joy in life shortly after childbirth, you might have postpartum depression. Contact your health care provider if you think you might be depressed, especially if your symptoms don't fade on their own, you have trouble caring for your baby or completing daily tasks, or you have thoughts of harming yourself or your baby.</p>
            </div>

            <br>
            <div id="weightloss">
                <h3 class="featurette-heading" style="color:#7d671f;">Weight loss
                </h3>

                <p class="lead1">After you give birth, you might look like you're still pregnant. This is normal. Most women lose 13 pounds (6 kilograms) during birth, including the weight of the baby, placenta and amniotic fluid. In the days after delivery, you'll lose additional weight from leftover fluids. After that, a healthy diet and regular exercise can help you gradually return to your pre-pregnancy weight.</p>
            </div>

            <br>
            <div id="postpartumcheck">
                <h3 class="featurette-heading" style="color:#7d671f;">The postpartum checkup
                </h3>

                <p class="lead1">IThe American College of Obstetricians and Gynecologists recommends that postpartum care be an ongoing process rather than just a single visit after your delivery. Contact your health care provider within the first three weeks after delivery. Within 12 weeks after delivery, see your health care provider for a comprehensive postpartum evaluation.
                </p>

                <p class="lead1">During this appointment, your health care provider will check your mood and emotional well-being, discuss contraception and birth spacing, and review information about infant care and feeding. Your health care provider will also talk about your sleep habits and issues related to fatigue, and do a physical exam. This might include checking your abdomen, vagina, cervix and uterus to make sure you're healing well. This is a great time to talk about any concerns you might have, including resuming sexual activity and how you're adjusting to life with a new baby.</p>
